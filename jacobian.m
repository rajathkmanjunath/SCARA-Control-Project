q_dot(2,1) = a1*cos(end_state(1))+a2*cos(end_state(1)+end_state(2));
q_dot(1,2) = a2*sin(end_state(1)+end_state(2));
q_dot(2,2) = a2*cos(end_state(1)+end_state(2));
